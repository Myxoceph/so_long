#include "../inc/libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	slen;

	slen = ft_strlen(s);
	while (s[slen] != (char) c)
	{
		slen--;
		if (slen == -1)
			return (NULL);
	}
	return ((char *)s + slen);
}
